export const Anisotrpia = (valor: string) => {
    if (valor == '0') return 'anisotropia_0'
    if (valor == '0.8') return 'anisotropia_0_8'
    if (valor == '1.0') return 'anisotropia_1'
    if (valor == '2.0') return 'anisotropia_2'
    if (valor == '3.0') return 'anisotropia_3'
    if (valor == '3.38') return 'anisotropia_3_38'
}